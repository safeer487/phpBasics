<?php

//For 
for ($i = 0; $i < 5; $i++) { 
	echo $i;
}

//While
$iNum = 0;
while ($iNum <= 10) {
	$iNum = $iNum + 1;
	echo $iNum;
}

//Do-while
$iNum2 = 0;
do {
	$iNum2 = $iNum2 + 1;
	echo $iNum2;
} while ($iNum2 <= 10);

//Foreach
$aMeses = {"Enero", "Febrero"...}
foreach ($aMeses as $valor) {
	echo $valor;
}

?>